export function translate(
  key: string,
  params?: Record<string, string | number>,
  lang: AppLang = getAppLang(),
): string {
  let s = lang === 'en' ? (EN[key] ?? key) : key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      s = s.replaceAll(`{${k}}`, String(v));
    }
  }
  return s;
}

// React hook: returns a `t` function bound to the current language and
// re-renders the component when the language toggle fires.
export function useT(): (key: string, params?: Record<string, string | number>) => string {
  const [lang, setLang] = useState<AppLang>(getAppLang());
  useEffect(() => {
    const handler = () => setLang(getAppLang());
    window.addEventListener('lily-lang-changed', handler);
    return () => window.removeEventListener('lily-lang-changed', handler);
  }, []);
  return (key, params) => translate(key, params, lang);
}
